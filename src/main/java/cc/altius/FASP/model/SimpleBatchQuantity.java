package cc.altius.FASP.model;

import cc.altius.FASP.framework.JsonDateDeserializer;
import cc.altius.FASP.framework.JsonDateSerializer;
import com.fasterxml.jackson.annotation.JsonView;
import com.fasterxml.jackson.databind.annotation.JsonDeserialize;
import com.fasterxml.jackson.databind.annotation.JsonSerialize;
import java.io.Serializable;
import java.util.Date;

/**
 *
 * @author akil
 */
public class SimpleBatchQuantity implements Serializable {

    @JsonView({Views.ArtmisView.class, Views.InternalView.class, Views.ReportView.class})
    private int batchId;
    @JsonView({Views.ArtmisView.class, Views.InternalView.class, Views.ReportView.class})
    private String batchNo;
    @JsonDeserialize(using = JsonDateDeserializer.class)
    @JsonSerialize(using = JsonDateSerializer.class)
    @JsonView({Views.ArtmisView.class, Views.InternalView.class, Views.ReportView.class})
    private Date expiryDate;
    @JsonView({Views.ArtmisView.class, Views.InternalView.class, Views.ReportView.class})
    private boolean autoGenerated;
    @JsonView({Views.ArtmisView.class, Views.InternalView.class, Views.ReportView.class})
    private double qty;
    @JsonView({Views.ArtmisView.class, Views.InternalView.class, Views.ReportView.class})
    private double qtyWps;
    @JsonView({Views.ArtmisView.class, Views.InternalView.class, Views.ReportView.class})
    private double expiredQty;
    @JsonView({Views.ArtmisView.class, Views.InternalView.class, Views.ReportView.class})
    private Double expiredQtyWps;
    @JsonView({Views.InternalView.class, Views.ReportView.class})
    private Double consumptionQty;
    @JsonView({Views.InternalView.class, Views.ReportView.class})
    private Double stockQty;
    @JsonView({Views.InternalView.class, Views.ReportView.class})
    private Double adjustmentQty;
    @JsonView({Views.InternalView.class, Views.ReportView.class})
    private Double shipmentQty;
    @JsonView({Views.InternalView.class, Views.ReportView.class})
    private Double shipmentQtyWps;
    @JsonView({Views.InternalView.class, Views.ReportView.class})
    private Double unallocatedQty;
    @JsonView({Views.InternalView.class, Views.ReportView.class})
    private Double unallocatedQtyWps;
    @JsonView({Views.InternalView.class, Views.ReportView.class})
    private double openingBalance;
    @JsonView({Views.InternalView.class, Views.ReportView.class})
    private double openingBalanceWps;

    @JsonDeserialize(using = JsonDateDeserializer.class)
    @JsonSerialize(using = JsonDateSerializer.class)
    @JsonView({Views.ArtmisView.class, Views.InternalView.class, Views.ReportView.class})
    private Date createdDate;

    public SimpleBatchQuantity() {
    }

    public SimpleBatchQuantity(int batchId, String batchNo, Date expiryDate, boolean autoGenerated, double qty, double qtyWps, double expiredQty, double expiredQtyWps, Date createdDate, Double shipmentQty, Double shipmentQtyWps) {
        this.batchId = batchId;
        this.batchNo = batchNo;
        this.expiryDate = expiryDate;
        this.autoGenerated = autoGenerated;
        this.qty = qty;
        this.qtyWps = qtyWps;
        this.expiredQty = expiredQty;
        this.expiredQtyWps = expiredQtyWps;
        this.createdDate = createdDate;
//        this.stockQty = stockQty;
        this.shipmentQty = shipmentQty;
        this.shipmentQtyWps = shipmentQtyWps;
    }

    public int getBatchId() {
        return batchId;
    }

    public void setBatchId(int batchId) {
        this.batchId = batchId;
    }

    public String getBatchNo() {
        return batchNo;
    }

    public void setBatchNo(String batchNo) {
        this.batchNo = batchNo;
    }

    public Date getExpiryDate() {
        return expiryDate;
    }

    public void setExpiryDate(Date expiryDate) {
        this.expiryDate = expiryDate;
    }

    public boolean isAutoGenerated() {
        return autoGenerated;
    }

    public void setAutoGenerated(boolean autoGenerated) {
        this.autoGenerated = autoGenerated;
    }

    public double getQty() {
        return qty;
    }

    public void setQty(double qty) {
        this.qty = qty;
    }

    public double getQtyWps() {
        return qtyWps;
    }

    public void setQtyWps(double qtyWps) {
        this.qtyWps = qtyWps;
    }

    public double getExpiredQty() {
        return expiredQty;
    }

    public void setExpiredQty(double expiredQty) {
        this.expiredQty = expiredQty;
    }

    public double getExpiredQtyWps() {
        return expiredQtyWps;
    }

    public void setExpiredQtyWps(double expiredQtyWps) {
        this.expiredQtyWps = expiredQtyWps;
    }

    public Date getCreatedDate() {
        return createdDate;
    }

    public void setCreatedDate(Date createdDate) {
        this.createdDate = createdDate;
    }

    public Double getConsumptionQty() {
        return consumptionQty;
    }

    public void setConsumptionQty(Double consumptionQty) {
        this.consumptionQty = consumptionQty;
    }

    public Double getStockQty() {
        return stockQty;
    }

    public void setStockQty(Double stockQty) {
        this.stockQty = stockQty;
    }

    public Double getAdjustmentQty() {
        return adjustmentQty;
    }

    public void setAdjustmentQty(Double adjustmentQty) {
        this.adjustmentQty = adjustmentQty;
    }

    public Double getShipmentQty() {
        return shipmentQty;
    }

    public void setShipmentQty(Double shipmentQty) {
        this.shipmentQty = shipmentQty;
    }

    public Double getUnallocatedQty() {
        return unallocatedQty;
    }

    public void setUnallocatedQty(Double unallocatedQty) {
        this.unallocatedQty = unallocatedQty;
    }

    public double getOpeningBalance() {
        return openingBalance;
    }

    public void setOpeningBalance(double openingBalance) {
        this.openingBalance = openingBalance;
    }

    public Double getShipmentQtyWps() {
        return shipmentQtyWps;
    }

    public void setShipmentQtyWps(Double shipmentQtyWps) {
        this.shipmentQtyWps = shipmentQtyWps;
    }

    public Double getUnallocatedQtyWps() {
        return unallocatedQtyWps;
    }

    public void setUnallocatedQtyWps(Double unallocatedQtyWps) {
        this.unallocatedQtyWps = unallocatedQtyWps;
    }

    public double getOpeningBalanceWps() {
        return openingBalanceWps;
    }

    public void setOpeningBalanceWps(double openingBalanceWps) {
        this.openingBalanceWps = openingBalanceWps;
    }

}
